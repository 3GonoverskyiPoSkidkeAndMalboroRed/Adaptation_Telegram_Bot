    dispatcher.add_handler(MessageHandler(Filters.regex('^–ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å –±–æ—Ç–∞$'), restart))
    
    # –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞
